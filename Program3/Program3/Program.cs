// 數字常見的用法 ( 整數 浮點數)
int num = 3;

Console.WriteLine("5 / 2 = " + 5.0 / 2);
Console.WriteLine("5 % 2 = " + 5 % 2);
Console.WriteLine("5 * 2 = " + 5 * 2);
Console.WriteLine((5 + 2) * num);

// 取絕對值
Console.WriteLine(Math.Abs(-10));

// 取次方 (基數,次方數)
Console.WriteLine(Math.Pow(2,3));

// 開根號 
Console.WriteLine(Math.Sqrt(36));

// 選最大數的
Console.WriteLine(Math.Max(122,100));

//選最小數的
Console.WriteLine(Math.Min(12,100));

// 四捨五入
Console.WriteLine(Math.Round(3.6));

